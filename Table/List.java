    private void YearKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_YearKeyTyped
        // TODO add your handling code here:
        
        if(evt.getKeyChar () == 10){
            model.setRowCount(0);
            model.setRowCount(6);
            
            int year = Integer.valueOf(Year.getText());
            int month = 0;
            int day = 1;
            
            Calendar cd = Calendar.getInstance();
            cd.set(year, month, day);
            int start = cd.get(cd.DAY_OF_WEEK);
            int maxday = cd.getActualMaximum(cd.DAY_OF_MONTH);
            
            start--;
            int j = 0;
            for(int i = 1; i <= maxday; i++){
                model.setValueAt(i, j, start);
                start++;
                if(start == 7){
                    start = 0;
                    j++;
                }
            }
        }
    }//GEN-LAST:event_YearKeyTyped

